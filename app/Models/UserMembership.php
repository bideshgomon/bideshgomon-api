<?php namespace App\Models;
use Illuminate\Database\Eloquent\Factories\HasFactory; use Illuminate\Database\Eloquent\Model; use Illuminate\Database\Eloquent\Relations\BelongsTo;
class UserMembership extends Model {
    use HasFactory;
    protected $fillable = ['user_id', 'organization_type_id', 'organization_name', 'membership_id', 'start_date', 'end_date'];
    protected $casts = ['start_date' => 'date', 'end_date' => 'date'];
    public function user(): BelongsTo { return $this->belongsTo(User::class); }
    public function organizationType(): BelongsTo { return $this->belongsTo(OrganizationType::class); }
}